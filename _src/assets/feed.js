/**
 * Generates Competing Videos
 */
function genVideos() {
    var videos = getRandom(feed, 13);

    for (var i = 0; i < 11; i++) {
        document.getElementById((i+1) + "-title").innerHTML = videos[i].title;
        document.getElementById((i+1) + "-name").innerHTML = videos[i].creator;
        document.getElementById((i+1) + "-thumbnail").style.backgroundImage = "url('" + videos[i].thumbnail + "')";
        document.getElementById((i+1) + "-avatar").src = videos[i].avatar;
        document.getElementById((i+1) + "-views").innerHTML = videos[i].views;
        document.getElementById((i+1) + "-time").innerHTML = videos[i].time;
        document.getElementById((i+1) + "-length").innerHTML = videos[i].length;
    }

    shuffle(document.querySelectorAll('#videofeed > .content'));
}

/**
 * Shuffles child elements
 * @param {*} elems the child elements to shuffle.
 */
function shuffle(elems) {
 
    allElems = (function(){
	var ret = [], l = elems.length;
	while (l--) { ret[ret.length] = elems[l]; }
	return ret;
    })();
 
    var shuffled = (function(){
        var l = allElems.length, ret = [];
        while (l--) {
            var random = Math.floor(Math.random() * allElems.length),
                randEl = allElems[random].cloneNode(true);
            allElems.splice(random, 1);
            ret[ret.length] = randEl;
        }
        return ret; 
    })(), l = elems.length;
 
    while (l--) {
        elems[l].parentNode.insertBefore(shuffled[l], elems[l].nextSibling);
        elems[l].parentNode.removeChild(elems[l]);
    }
 
}

/**
 * Selects n random elements from an array
 * @param {*} arr the array to choose n random elements from
 * @param {*} n the amount of elements to choose from the array
 * @returns n random elements of an array
 */
function getRandom(arr, n) {
    var result = new Array(n),
        len = arr.length,
        taken = new Array(len);
    if (n > len)
        throw new RangeError("getRandom: more elements taken than available");
    while (n--) {
        var x = Math.floor(Math.random() * len);
        result[n] = arr[x in taken ? taken[x] : x];
        taken[x] = --len in taken ? taken[len] : len;
    }
    return result;
}

